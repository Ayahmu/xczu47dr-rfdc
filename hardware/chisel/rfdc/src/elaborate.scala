  proc outclk_freq_hz {} { return $outclkFreqHz }
  proc associated_busif {} { return {S_AXIS_20:S_AXIS_22:S_AXIS_30:S_AXIS_32} }

  proc config {} {
    return [list \\
$body]
  }
}
"""
  }
}
