package rfdc

import java.nio.charset.StandardCharsets
import java.nio.file.{Files, Paths}

object elaborate extends App {
  private val outputDir = Paths.get("generated")
  Files.createDirectories(outputDir)

  private val outputPath = outputDir.resolve("rfdc_custom_xczu47dr_config.tcl")
  private val config = RfdcCustomXczu47drConfig.renderTcl
  Files.write(outputPath, config.getBytes(StandardCharsets.UTF_8))
  println(s"RFDC Vivado configuration generated: ${outputPath.toString}")
}

